package tomorrow.tomo.guis.musicPlayer;

public class Music {
    public String name;
    public String description;
    public String pic;
    public String id;
    public String url;

    public Music(String name, String description, String pic, String id, String url) {
        this.name = name;
        this.description = description;
        this.pic = pic;
        this.id = id;
        this.url = url;

    }

}
